package com.jbetfairng.entities;

import java.util.Date;

public class TimeRange {

    private Date from;
    private Date to;

    public final Date getFrom() {
        return from;
    }

    public final void setFrom(Date from) {
        this.from = from;
    }

    public final Date getTo() {
        return to;
    }

    public final void setTo(Date to) {
        this.to = to;
    }

}
